BITMASK = (1<<39)

def render_string(text, font=charset_5x7, char='#', nochar=' ', blank=' '):
	s = [font[ord(c)] for c in text]

	n = ''
	for y in range(7):
		for c in s:
			for x in range(5):
				if c & (BITMASK>>(y + (8*x))):
					n += char
				else:
					n += nochar
			n += blank
		n += '\n'
	return n
	
def yield_string_bits(text, font=charset_5x7):
	s = [font[ord(c)] for c in text]
	for y in range(7):
		for c in s:
			for x in range(0,40,8):
				yield c & (BITMASK>>(y + x))

